package org.halemaster.flexibilities;

public class Effect
{
    private int effectId;
    private int level;
    private int time;

    public int getEffectId()
    {
        return effectId;
    }

    public void setEffectId(int effectId)
    {
        this.effectId = effectId;
    }

    public int getLevel()
    {
        return level;
    }

    public void setLevel(int level)
    {
        this.level = level;
    }

    public int getTime()
    {
        return time;
    }

    public void setTime(int time)
    {
        this.time = time;
    }
}
